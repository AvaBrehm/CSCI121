
public class DinnerParty extends Party {

    // Private instance variable to store the dinner choice
    private int dinnerChoice;

    // Method to set the dinner choice
    public void setDinnerChoice(int choice) {
        // Set the value of dinnerChoice to the provided choice
        dinnerChoice = choice;
    }

    // Method to retrieve the dinner choice
    public int getDinnerChoice() {
        // Return the value of dinnerChoice
        return dinnerChoice;
    }
}


